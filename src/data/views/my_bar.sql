CREATE OR REPLACE FORCE EDITIONABLE VIEW "APP_USER"."MY_BAR" (
  "MAIN_USER_ID",
  "COCKTAIL_RESOURCE_ID",
  "NAME",
  "TYPE",
  "ALCOHOL_PERCENTAGE",
  "HAVE_IT",
  "WANT_IT"
) DEFAULT COLLATION "USING_NLS_COMP" AS
  SELECT
    B.MAIN_USER_ID,
    B.COCKTAIL_RESOURCE_ID,
    CR.NAME,
    CR.TYPE,
    CR.ALCOHOL_PERCENTAGE,
    B.HAVE_IT,
    B.WANT_IT
  FROM
    BAR B
      INNER JOIN COCKTAIL_RESOURCE CR
      ON B.COCKTAIL_RESOURCE_ID = CR.ID;