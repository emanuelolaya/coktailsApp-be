CREATE OR REPLACE FORCE EDITIONABLE VIEW "APP_USER"."SHOPING_LIST" (
  "MAIN_USER_ID",
  "COCKTAIL_RESOURCE_ID",
  "NAME",
  "SHORT_DESCRIPTION",
  "DESCRIPTION",
  "IMG_URL",
  "TYPE",
  "ALCOHOL_PERCENTAGE"
) DEFAULT COLLATION "USING_NLS_COMP" AS
  SELECT
    B.MAIN_USER_ID,
    B.COCKTAIL_RESOURCE_ID,
    CR.NAME,
    CR.SHORT_DESCRIPTION,
    CR.DESCRIPTION,
    CR.IMG_URL,
    CR.TYPE,
    CR.ALCOHOL_PERCENTAGE
  FROM
    BAR B
      INNER JOIN COCKTAIL_RESOURCE CR
      ON B.COCKTAIL_RESOURCE_ID = CR.ID
  WHERE
    B.WANT_IT = 1;